function validate(){
	
	var filename=$('#empfile').val().split('.').pop().toLowerCase();

	
	if(filename=="xlsx" || filename=="xls"){
		
	
		
		return true;
	}else{
		
		$("#validateTable").show();
		$(".validateTips").text("please select only excel file");
		
		
		return false;
	}
	
	
} 

$(function(){
	
	$("#validateTable").hide();
});
	