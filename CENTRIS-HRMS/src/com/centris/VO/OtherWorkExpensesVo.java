package com.centris.VO;

public class OtherWorkExpensesVo {
	
	private int sno;
	private String date;
	private String description;
	private double amount;
	private String other_expensecode;
	
	
	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public String getOther_expensecode() {
		return other_expensecode;
	}
	public void setOther_expensecode(String other_expensecode) {
		this.other_expensecode = other_expensecode;
	}

	
	
}
