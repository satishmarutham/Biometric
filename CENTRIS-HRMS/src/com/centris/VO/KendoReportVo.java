package com.centris.VO;

public class KendoReportVo {
	private String empDep;
	private String empPresent;
	private String empAbsent;

	public String getEmpDep() {
		return empDep;
	}

	public void setEmpDep(String empDep) {
		this.empDep = empDep;
	}

	public String getEmpPresent() {
		return empPresent;
	}

	public void setEmpPresent(String empPresent) {
		this.empPresent = empPresent;
	}

	public String getEmpAbsent() {
		return empAbsent;
	}

	public void setEmpAbsent(String empAbsent) {
		this.empAbsent = empAbsent;
	}

}