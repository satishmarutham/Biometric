package com.centris.VO;

public class TelephoneExpensesVO {

	private int teleexp_sno;
	private String teleexp_date;
	private String teleexp_mobileno;
	private String teleexp_location;
	private double teleexp_amount;
	private String teleexpcode;
	
	
	
	public int getTeleexp_sno() {
		return teleexp_sno;
	}
	public void setTeleexp_sno(int teleexp_sno) {
		this.teleexp_sno = teleexp_sno;
	}
	public String getTeleexp_date() {
		return teleexp_date;
	}
	public void setTeleexp_date(String teleexp_date) {
		this.teleexp_date = teleexp_date;
	}
	public String getTeleexp_mobileno() {
		return teleexp_mobileno;
	}
	public void setTeleexp_mobileno(String teleexp_mobileno) {
		this.teleexp_mobileno = teleexp_mobileno;
	}
	public String getTeleexp_location() {
		return teleexp_location;
	}
	public void setTeleexp_location(String teleexp_location) {
		this.teleexp_location = teleexp_location;
	}
	public double getTeleexp_amount() {
		return teleexp_amount;
	}
	public void setTeleexp_amount(double teleexp_amount) {
		this.teleexp_amount = teleexp_amount;
	}
	public String getTeleexpcode() {
		return teleexpcode;
	}
	public void setTeleexpcode(String teleexpcode) {
		this.teleexpcode = teleexpcode;
	}
	
	
	
	
	
	
	
	
	
	
	
	
}
