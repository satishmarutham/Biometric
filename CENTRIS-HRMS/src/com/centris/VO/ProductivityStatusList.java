package com.centris.VO;

public class ProductivityStatusList {
	
	private String working_hrs;
	private String total_hrs;
	private String permission_hrs;
	private String empname;
	public String getWorking_hrs() {
		return working_hrs;
	}
	public void setWorking_hrs(String working_hrs) {
		this.working_hrs = working_hrs;
	}
	public String getTotal_hrs() {
		return total_hrs;
	}
	public void setTotal_hrs(String total_hrs) {
		this.total_hrs = total_hrs;
	}
	public String getPermission_hrs() {
		return permission_hrs;
	}
	public void setPermission_hrs(String permission_hrs) {
		this.permission_hrs = permission_hrs;
	}
	public String getEmpname() {
		return empname;
	}
	public void setEmpname(String empname) {
		this.empname = empname;
	}
	
	
	
	
	
	
	
}
