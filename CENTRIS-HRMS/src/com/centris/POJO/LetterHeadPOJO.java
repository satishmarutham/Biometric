package com.centris.POJO;

public class LetterHeadPOJO {

	private String letterheadcode;
	private String lettertype;
	private String purpose;
	private String description;
	private String createdby;
	
	
	public String getLetterheadcode() {
		return letterheadcode;
	}
	public void setLetterheadcode(String letterheadcode) {
		this.letterheadcode = letterheadcode;
	}
	public String getLettertype() {
		return lettertype;
	}
	public void setLettertype(String lettertype) {
		this.lettertype = lettertype;
	}
	public String getPurpose() {
		return purpose;
	}
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getCreatedby() {
		return createdby;
	}
	public void setCreatedby(String createdby) {
		this.createdby = createdby;
	}
	
	
	
	
	
	
	
	
	
	
	
}
